static const uint8_t* charsPrvGetImage(char ch)
{
	uint_fast16_t chr = (unsigned char)ch;
	const uint8_t* cc = chars;
	
	if (chr < FIRST_VALID_CHAR || chr > LAST_VALID_CHAR)
		chr = MISSING_CHAR_CHAR;
	chr -= FIRST_VALID_CHAR;
	
	cc += (chr * 6);
	
	return cc;
}

static bool charsPrvUnpackChar(const uint8_t* chr, uint_fast8_t row, uint_fast8_t col)
{
	if (col >= CHAR_WIDTH)
		col = 0;
	
	//each 3 bytes code for 4 rows of the symbol
	while (row >= 4) {
		
		chr += 3;
		row -= 4;
	}
	
	while (row--)
		col += CHAR_WIDTH;	//sol is now the bit we want
	
	chr += (col / 8);
	
	col = col % 8;
	
	return ((*chr) >> col) & 1;
}

void charsPrvDrawOne(uint8_t *framebuffer, uint32_t w, uint32_t h, int32_t r, int32_t c, char ch, uint_fast8_t foreColor, uint_fast8_t backColor)
{
	const uint8_t *chrImg = charsPrvGetImage(ch);
	uint_fast8_t dr, dc;
	
	for (dr = 0; dr < CHAR_HEIGHT; dr++) {
		
		int32_t er = r + (int_fast8_t)dr;
		
		if (er < 0)
			continue;
		if ((uint32_t)er >= h)
			return;
		
		for (dc = 0; dc < CHAR_WIDTH; dc++) {
			
			int32_t ec = c + (int_fast8_t)dc;
			uint_fast8_t color;
			uint32_t pixelNum;
			uint8_t *pix;
			
			if (ec < 0)
				continue;
			if ((uint32_t)ec >= w)
				break;
			
			pixelNum = er * w + ec;
			color = charsPrvUnpackChar(chrImg, dr, dc) ? foreColor : backColor;
			pix = &framebuffer[pixelNum / 2];
			*pix = ((*pix) &~ (0x0f << ((pixelNum % 2) * 4))) | (color << ((pixelNum % 2) * 4));
		}
	}
}

